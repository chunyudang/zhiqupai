export async function seedQuestions(prisma: PrismaClient) {
  console.log('Creating questions...')

  // Fetch all categories with their levels
  const categories = await prisma.category.findMany({
    include: {
      levels: {
        orderBy: { levelNo: 'asc' },
      },
    },
  })

  let questionCount = 0

  for (const cat of categories) {
    const catQuestions = allQuestions[cat.nameEn]
    if (!catQuestions) {
      console.log(`  ⚠️  No question data found for category: ${cat.nameEn}`)
      continue
    }

    // cat.levels is already sorted by levelNo (asc)
    for (let levelIdx = 0; levelIdx < cat.levels.length; levelIdx++) {
      const level = cat.levels[levelIdx]
      const levelQuestions = catQuestions[levelIdx]

      if (!levelQuestions) {
        console.log(
          `  ⚠️  No question data found for level: ${cat.nameEn} L${level.levelNo}`,
        )
        continue
      }

      for (let qi = 0; qi < levelQuestions.length; qi++) {
        const q = levelQuestions[qi]
        const correctAnswer = idxToLetter(q.correct)
        const options = q.opts.map(
          (opt, i) => `${idxToLetter(i)}. ${opt}`,
        )

        await prisma.question.create({
          data: {
            levelId: level.id,
            questionType: 'choice',
            content: q.content,
            options: JSON.stringify(options),
            correctAnswer,
            explanation: q.exp,
            sortOrder: qi + 1,
            status: 'active',
          },
        })
        questionCount++
      }
    }
  }

  console.log(`  ${questionCount} questions created`)
}
